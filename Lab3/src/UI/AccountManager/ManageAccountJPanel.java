    private void ViewDetailsBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewDetailsBActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow >= 0){
            Account selectAccount = (Account) jTable1.getValueAt(selectedRow, 0);
              
                ViewAccountJPanel panel = new ViewAccountJPanel(userProcessContainer, accountdirectory, selectAccount);
                userProcessContainer.add("ViewAccountJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
         
        }else {
            JOptionPane.showMessageDialog(null, "Please select an account from tht list of view.","Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ViewDetailsBActionPerformed

    private void SearchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBActionPerformed
        // TODO add your handling code here:
        if (!SearchT.getText().isBlank()){
            String accountNumber = SearchT.getText();
            Account foundAccount = accountdirectory.searchAccount(accountNumber);
            
            if (foundAccount !=null){
                
                ViewAccountJPanel panel = new ViewAccountJPanel(userProcessContainer, accountdirectory,foundAccount);
                userProcessContainer.add("ViewAccountJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
         
                
            }else {
                       JOptionPane.showMessageDialog(null,"Account not found. Please check the Account Number and try again " ,"warning", JOptionPane.WARNING_MESSAGE);

            }
        }else {
                   JOptionPane.showMessageDialog(null,"Please type the account Number to view " ,"warning", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_SearchBActionPerformed


