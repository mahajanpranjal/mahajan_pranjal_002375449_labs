   public void populateTable(){
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
       model.setRowCount(0);
       
        for (Account a : accountdirectory.getAccount()){
            Object[] row = new Object[4];
            row[0] = a;
            row[1] = a.getRoutingNumber();
            row[2] = a.getAccountNumber();
            row[3] = a.getBalance();
            
            model.addRow(row);  
        }
   }
